create or replace function public.normalize_participant_session(p_session_id uuid)
returns void
language plpgsql
security definer
set search_path = public
as $$
declare
  session_row public.participant_sessions%rowtype;
  page_key text;
  page_value jsonb;
  page_response_id uuid;
  phase_name text;
  phase_value jsonb;
  sample_value jsonb;
  sample_index integer;
begin
  select *
  into session_row
  from public.participant_sessions
  where id = p_session_id;

  if not found then
    return;
  end if;

  delete from public.page_responses
  where session_id = session_row.id;

  for page_key, page_value in
    select key, value
    from jsonb_each(coalesce(session_row.session_payload->'pages', '{}'::jsonb))
  loop
    insert into public.page_responses (
      session_id,
      participant_id,
      participant_number,
      study_id,
      page_id,
      page_title,
      family_id,
      family_label,
      case_id,
      case_title,
      template,
      image_set_id,
      question,
      selection,
      selected_label,
      stimulus_time_on_page_ms,
      selection_popup_time_on_page_ms,
      combined_time_on_page_ms,
      stimulus_valid_sample_count,
      stimulus_invalid_sample_count,
      selection_popup_valid_sample_count,
      selection_popup_invalid_sample_count,
      combined_valid_sample_count,
      combined_invalid_sample_count
    )
    values (
      session_row.participant_id,
      session_row.participant_id,
      session_row.participant_number,
      session_row.study_id,
      page_key,
      page_value->>'pageTitle',
      page_value->>'familyId',
      page_value->>'familyLabel',
      page_value->>'caseId',
      page_value->>'caseTitle',
      page_value->>'template',
      page_value->>'imageSetId',
      page_value->>'question',
      page_value->>'selection',
      page_value->>'selectedLabel',
      coalesce((page_value->>'timeOnPageMs')::integer, 0),
      coalesce((page_value->>'selectionPopupTimeOnPageMs')::integer, 0),
      coalesce((page_value->>'timeOnPageMs')::integer, 0) +
        coalesce((page_value->>'selectionPopupTimeOnPageMs')::integer, 0),
      coalesce((page_value->>'validSampleCount')::integer, 0),
      coalesce((page_value->>'invalidSampleCount')::integer, 0),
      coalesce((page_value->>'selectionPopupValidSampleCount')::integer, 0),
      coalesce((page_value->>'selectionPopupInvalidSampleCount')::integer, 0),
      coalesce((page_value->>'validSampleCount')::integer, 0) +
        coalesce((page_value->>'selectionPopupValidSampleCount')::integer, 0),
      coalesce((page_value->>'invalidSampleCount')::integer, 0) +
        coalesce((page_value->>'selectionPopupInvalidSampleCount')::integer, 0)
    )
    returning id
    into page_response_id;

    foreach phase_name in array array['stimulus', 'selectionPopup']
    loop
      phase_value := coalesce(page_value->'phases'->phase_name, '{}'::jsonb);

      insert into public.page_phase_metrics (
        page_response_id,
        session_id,
        participant_id,
        participant_number,
        study_id,
        page_id,
        phase,
        started_at,
        ended_at,
        time_on_page_ms,
        valid_sample_count,
        invalid_sample_count
      )
      values (
        page_response_id,
        session_row.id,
        session_row.participant_id,
        session_row.participant_number,
        session_row.study_id,
        page_key,
        phase_name,
        nullif(phase_value->>'startedAt', '')::timestamptz,
        nullif(phase_value->>'endedAt', '')::timestamptz,
        coalesce((phase_value->>'timeOnPageMs')::integer, 0),
        coalesce((phase_value->>'validSampleCount')::integer, 0),
        coalesce((phase_value->>'invalidSampleCount')::integer, 0)
      );

      sample_index := 0;
      for sample_value in
        select value
        from jsonb_array_elements(coalesce(phase_value->'gazePoints', '[]'::jsonb))
      loop
        insert into public.gaze_samples (
          page_response_id,
          session_id,
          participant_id,
          participant_number,
          study_id,
          page_id,
          phase,
          sample_index,
          sample_timestamp,
          elapsed_time_ms,
          valid,
          raw_x,
          raw_y,
          x,
          y,
          relative_x,
          relative_y,
          page_width,
          page_height,
          in_bounds
        )
        values (
          page_response_id,
          session_row.id,
          session_row.participant_id,
          session_row.participant_number,
          session_row.study_id,
          page_key,
          phase_name,
          sample_index,
          nullif(sample_value->>'timestamp', '')::timestamptz,
          nullif(sample_value->>'elapsedTimeMs', '')::integer,
          coalesce((sample_value->>'valid')::boolean, false),
          nullif(sample_value->>'rawX', '')::double precision,
          nullif(sample_value->>'rawY', '')::double precision,
          nullif(sample_value->>'x', '')::double precision,
          nullif(sample_value->>'y', '')::double precision,
          nullif(sample_value->>'relativeX', '')::double precision,
          nullif(sample_value->>'relativeY', '')::double precision,
          nullif(sample_value->>'pageWidth', '')::double precision,
          nullif(sample_value->>'pageHeight', '')::double precision,
          coalesce((sample_value->>'inBounds')::boolean, false)
        );
        sample_index := sample_index + 1;
      end loop;
    end loop;
  end loop;
end;
$$;

create or replace function public.rebuild_normalized_participant_data()
returns void
language plpgsql
security definer
set search_path = public
as $$
declare
  session_entry record;
begin
  truncate table public.gaze_samples restart identity cascade;
  truncate table public.page_phase_metrics cascade;
  truncate table public.page_responses cascade;

  for session_entry in
    select id from public.participant_sessions
  loop
    perform public.normalize_participant_session(session_entry.id);
  end loop;
end;
$$;

