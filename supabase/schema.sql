create or replace function public.submit_participant_session(payload jsonb)
returns table (participant_number bigint, duplicate boolean)
language plpgsql
security definer
set search_path = public
as $$
declare
  inserted_number bigint;
  inserted_id uuid;
begin
  insert into public.participant_sessions (
    participant_id,
    study_id,
    consent_timestamp,
    completed_at,
    age_category,
    province,
    gender_identity,
    online_shopping_frequency,
    primary_shopping_device,
    retailer_familiarity,
    submission_source,
    participant_profile,
    device_info,
    page_summary,
    total_valid_samples,
    total_invalid_samples,
    session_payload
  )
  values (
    payload->>'participant_id',
    payload->>'study_id',
    nullif(payload->>'consent_timestamp', '')::timestamptz,
    nullif(payload->>'completed_at', '')::timestamptz,
    nullif(payload->>'age_category', ''),
    nullif(payload->>'province', ''),
    nullif(payload->>'gender_identity', ''),
    nullif(payload->>'online_shopping_frequency', ''),
    nullif(payload->>'primary_shopping_device', ''),
    nullif(payload->>'retailer_familiarity', ''),
    coalesce(nullif(payload->>'submission_source', ''), 'github-pages'),
    coalesce(payload->'participant_profile', '{}'::jsonb),
    coalesce(payload->'device_info', '{}'::jsonb),
    coalesce(payload->'page_summary', '[]'::jsonb),
    coalesce((payload->>'total_valid_samples')::integer, 0),
    coalesce((payload->>'total_invalid_samples')::integer, 0),
    coalesce(payload->'session_payload', '{}'::jsonb)
  )
  on conflict (participant_id) do nothing
  returning public.participant_sessions.id, public.participant_sessions.participant_number
  into inserted_id, inserted_number;

  if inserted_number is not null then
    perform public.normalize_participant_session(inserted_id);
    return query select inserted_number, false;
    return;
  end if;

  select participant_sessions.id, participant_sessions.participant_number
  into inserted_id, inserted_number
  from public.participant_sessions
  where participant_sessions.participant_id = payload->>'participant_id';

  if inserted_id is not null and not exists (
    select 1 from public.page_responses where session_id = inserted_id
  ) then
    perform public.normalize_participant_session(inserted_id);
  end if;

  return query select inserted_number, true;
end;
$$;

